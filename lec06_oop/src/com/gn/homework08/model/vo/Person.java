package com.gn.homework08.model.vo;

public class Person {
	public String name;
	public int age;
	
	public Person() {

	}

	//getter
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	//setter
	public void setName(String name) {
		this.name=name;
	}
	public void setAge(int age) {
		this.age=age;
	}
	
}
